# To’g’ri to’rtburchakning tomonlari a va b berilgan. Uning yuzasi S = a*b orqali va P=2*(a+b) orqali aniqlansin.
a = int(input("To'g'ri to'rtburchakning a tomonini kiriting: a = ")) # a tomon qiymati
b = int(input("To'g'ri to'rtburchakning b tomonini kiriting: b = ")) # b tomon qiymati
S = a*b # to'g'ri to'rtburchakning yuzasini hisoblash formulasi
P = 2*(a+b) # to'g'ri to'rtburchakning perimetrini hisoblash formulasi
print(f"To'g'ri to'rtburchakning yuzasi {S} ga va perimetri {P} ga teng.") # natija